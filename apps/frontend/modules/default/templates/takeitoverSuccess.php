<h1>Help Us!</h1>
<p>There are many ways to help us:</p>
<p>
	Please <b>set links</b> to Claimer.org in your social network page(s), in your blog(s) or in other websites!<br />
	Spread the word that damages caused by climate change should be claimed.
</p>
<ul>
  <li>Are you ready to <b>help us find organizations and lawyers</b> interested in climate and other environmental damage registration? If so, please just start and send us recommendations!</li>
  <li>If you personally know somebody working for an NGO or law firm which could use Claimer.org, please write to her or him directly to attract her or his attention to Claimer.org!</li>
  <li>If, by your position or experience, you can help us <b>finding campaigning partners</b> or if you would like to give us <b>strategy advice</b>, please contact us!</li>
  <li>If you can <b>promote new bills / acts / laws</b> that make climate and other environmental polluters jointly and commonly liable, please just do so and keep us informed!</li>
  <li>If you see another way of promoting the idea of climate and other environmental damage registration and compensation, please let us know!</li>
  <li>If you could <b>improve or extend our software</b> or if you can assist us otherwise, please let us know as well!</li>
</ul>
<p>Whatever you do for us, please <b>remember our strategy</b>:</p>
<p>Our goal is to promote the idea of climate and other environmental damage registration and compensation. Claimer.org and its software is a tool to this end.</p>
<p>We are not interested in damage data ownership. We prefer our partners to install the Claimer.org software on their own servers (without access for us).</p>
<p>We do not have any commercial interests. We would even hand-over our website and software if a powerful institution or network with an excellent reputation was ready to take them over.</p>
<p>However, we are also interested in co-operating with partners who have commercial interests if it serves the overall non-profit purpose.</p>
